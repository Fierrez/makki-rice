#!/usr/bin/env node
// =============================================================================
// tools/dev/gen-theme-vars.mjs — Theme → SCSS Variable Generator
// =============================================================================
// Reads a theme JS file and regenerates ui-engine/ags/style/variables.scss
// so the palette section always matches the active theme.
//
// Usage:
//   node tools/dev/gen-theme-vars.mjs mocha
//   node tools/dev/gen-theme-vars.mjs latte
//   node tools/dev/gen-theme-vars.mjs    (uses current theme from cache)
// =============================================================================

import { readFileSync, writeFileSync, existsSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";
import { createRequire } from "module";

const __dirname = dirname(fileURLToPath(import.meta.url));
const riceRoot  = resolve(__dirname, "../..");

// ─── Resolve theme ────────────────────────────────────────────────────────────
let themeId = process.argv[2];

if (!themeId) {
    const cacheFile = `${process.env.HOME}/.cache/makki-rice/current-theme`;
    if (existsSync(cacheFile)) {
        themeId = readFileSync(cacheFile, "utf8").trim();
        console.log(`Using cached theme: ${themeId}`);
    } else {
        themeId = "mocha";
        console.log(`No cached theme — defaulting to: ${themeId}`);
    }
}

const themePath = resolve(riceRoot, `config/themes/catppuccin-${themeId}.js`);

if (!existsSync(themePath)) {
    console.error(`Theme file not found: ${themePath}`);
    console.error(`Available: mocha, latte, frappe, macchiato`);
    process.exit(1);
}

// ─── Dynamic import ───────────────────────────────────────────────────────────
const { default: theme } = await import(`file://${themePath}`);
const { colors, accents, meta } = theme;

// ─── Generate SCSS ────────────────────────────────────────────────────────────
const isDark = meta.variant === "dark";

const scss = `// =============================================================================
// variables.scss — GENERATED by tools/dev/gen-theme-vars.mjs
// Theme: ${meta.name} (${meta.variant})
// DO NOT EDIT MANUALLY — run: node tools/dev/gen-theme-vars.mjs ${themeId}
// =============================================================================

// ─── ${meta.name} Palette ──────────────────────────────────────────────────
$rosewater:   ${colors.rosewater};
$flamingo:    ${colors.flamingo};
$pink:        ${colors.pink};
$mauve:       ${colors.mauve};
$red:         ${colors.red};
$maroon:      ${colors.maroon};
$peach:       ${colors.peach};
$yellow:      ${colors.yellow};
$green:       ${colors.green};
$teal:        ${colors.teal};
$sky:         ${colors.sky};
$sapphire:    ${colors.sapphire};
$blue:        ${colors.blue};
$lavender:    ${colors.lavender};
$text:        ${colors.text};
$subtext1:    ${colors.subtext1};
$subtext0:    ${colors.subtext0};
$overlay2:    ${colors.overlay2};
$overlay1:    ${colors.overlay1};
$overlay0:    ${colors.overlay0};
$surface2:    ${colors.surface2};
$surface1:    ${colors.surface1};
$surface0:    ${colors.surface0};
$base:        ${colors.base};
$mantle:      ${colors.mantle};
$crust:       ${colors.crust};

// ─── Semantic Tokens ──────────────────────────────────────────────────────────
$bg-primary:    $base;
$bg-secondary:  $mantle;
$bg-surface:    $surface0;
$bg-overlay:    rgba($surface0, 0.85);

$accent-primary:   $blue;
$accent-secondary: $mauve;
$accent-tertiary:  $green;

$text-primary:   $text;
$text-secondary: $subtext1;
$text-muted:     $overlay2;

$border-color:   rgba($surface2, 0.6);
$shadow-color:   rgba($crust, 0.7);

// ─── Spacing ──────────────────────────────────────────────────────────────────
$space-xs:  4px;
$space-sm:  8px;
$space-md:  12px;
$space-lg:  16px;
$space-xl:  24px;
$space-2xl: 32px;

// ─── Border Radius ────────────────────────────────────────────────────────────
$radius-sm:   6px;
$radius-md:   10px;
$radius-lg:   14px;
$radius-xl:   20px;
$radius-pill: 100px;

// ─── Typography ───────────────────────────────────────────────────────────────
$font-ui:   "JetBrains Mono Nerd Font", "Noto Sans", sans-serif;
$font-mono: "JetBrains Mono Nerd Font", monospace;
$font-size-sm:   12px;
$font-size-base: 14px;
$font-size-lg:   16px;
$font-size-xl:   20px;
$font-size-2xl:  28px;

// ─── Animation ────────────────────────────────────────────────────────────────
$transition-fast:   150ms ease;
$transition-normal: 250ms ease;
$transition-slow:   400ms cubic-bezier(0.34, 1.56, 0.64, 1);

// ─── Blur ─────────────────────────────────────────────────────────────────────
$blur-amount: 20px;
`;

// ─── Write output ─────────────────────────────────────────────────────────────
const outPath = resolve(riceRoot, "ui-engine/ags/style/variables.scss");
writeFileSync(outPath, scss, "utf8");
console.log(`✓ Written: ${outPath}`);
console.log(`  Theme: ${meta.name} | Variant: ${meta.variant}`);
